// java chapter-6/Calculator.java

public class Calculator {
    public int performAddition(int a, int b) {
        int result = a + b;
        return result;
    }

    public int performAddition(float a, float b) {
        float result = a + b;
        return result;
    }

    public double performAddition(double a, double b) {
        double result = a + b;
        return result;
    }
}